
public class AssignmentOperators 
{
	public static void main(String args[]) 
	{
	
	       int A = 10;
	       A += 10;    // A = A +10;     // Assignment operator ->  (+) plus Assignment Operator
	       System.out.print(" (+) Assignment Operator --->  ");             
	       System.out.println(A);
	
	
	       
	
	      int B  = 5; 
	      B -= 5;   // B = B - 5;      // Assignment operator ->    (-) Minus Assignment Operator
	      System.out.print(" (-) Assignment Operator --->  ");
	      System.out.println(B);
	
	
	
	     B = 5;
	     B *= 5;   // B = B * 5;      // Assignment operator ->    (*) Multiply Assignment Operator
	     System.out.print(" (*) Assignment Operator --->  ");
	     System.out.println(B);
	
	
	
	      B = 5;
	      B /= 5;    // B = B / 5;    // Assignment operator ->   (/)  Divide Assignment Operator
	      System.out.print(" (/) Assignment Operator --->  ");
	      System.out.println(B);
	
	
	
	
	      B = 5; 
	      B %= 5;   //B = B % 5;     //  Assignment operator ->  (%) modulo Assignment Operator
	      System.out.print(" (%) Assignment Operator --->  ");
	      System.out.println(B);
	
			
	}

}
