

public class BitwiseOperators 
{
	  public static void main(String args[])      
	    {
	        System.out.println("Binary AND(&) of 5 & 6 = " + (5 & 6));               // Binary AND(&)

	        System.out.println("Binary OR(|) of 5 | 6 = " + (5 | 6));                 // Binary OR(|)

	        System.out.println("Binary XOR(^) of 5 ^ 6 = " + (5 ^ 6));                 // Binary XOR(^)

	        System.out.println("Binary 1's Complement(~) of (~5) = " + (~5));          // Binary 1's Complement(~)

	        System.out.println("Binary Left Shift(<<) of 5 << 2 = " + (5<<2));          // Binary Left Shift(<<)

	        System.out.println("Binary Right Shift(>>) of 6 >> 1 = " + (6>>1));           // Binary Right Shift(>>)

	    }

}
